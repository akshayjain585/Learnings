package javatpointLearnings;

/*1. Class implementing multiple inheritance.
2. Can be achieved since the implementation is provided in main class.
3. Interface is good for security as well by giving the reference of parent class to make only the parent's class method visible while creating the objects.
*/
public class MultipleInheritanceExample implements Honda,Maruti{

	public static void main(String[] args) {
		
		
		MultipleInheritanceExample obj = new MultipleInheritanceExample();
		obj.Brand();

	}
	public void Brand() {

		System.out.println("The brand is of Maruti");
	}
	
}

interface Honda{
	
	public void Brand();
}

interface Maruti{
	
public void Brand();
}
